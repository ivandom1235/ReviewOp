from __future__ import annotations

from typing import Any

from report_context import ReportContext


def assemble_pipeline_report(*, context: ReportContext) -> dict[str, Any]:
    cfg = context.cfg
    pipeline_state = context.pipeline_state
    train = pipeline_state["train"]
    benchmark = pipeline_state["benchmark"]
    evaluation = pipeline_state["evaluation"]
    governance = pipeline_state["governance"]
    report = {
        "pipeline_version": "6.0-v6-only",
        "output_version": cfg.output_version,
        "generated_at": context.generated_at,
        "run_profile": context.run_profile,
        "artifact_mode": context.artifact_mode,
        "config": context.config,
        "text_column": context.text_column,
        "schema": context.schema,
        "implicit_mode": cfg.implicit_mode,
        "multilingual_mode": cfg.multilingual_mode,
        "domain_conditioning_mode": context.train_domain_conditioning_mode,
        "train_domain_conditioning_mode": context.train_domain_conditioning_mode,
        "eval_domain_conditioning_mode": context.eval_domain_conditioning_mode,
        "coreference_enabled": cfg.use_coref,
        "language_distribution": context.prepared_language_distribution,
        "research": context.research,
        "stratification_choice": "group_id",
        "candidate_aspects": context.candidate_aspects,
        "candidate_aspects_by_language": context.candidate_aspects_by_language,
        "candidate_aspects_by_domain": context.candidate_aspects_by_domain_train,
        "chunk_preview_size": len(context.chunk_preview),
        "chunk_sampling_strategy": "seeded_shuffle_then_slice",
        "row_counts": {
            "input": len(context.frame),
            "preprocessed": len(context.prepared),
            "selected": len(context.sample_frame),
            "train": len(context.train_built),
            "val": len(context.val_built),
            "test": len(context.test_built),
            "train_export": len(train["export_rows"]),
        },
        "split_ratios": {
            "train": round(len(context.train_built) / max(1, len(context.train_built) + len(context.val_built) + len(context.test_built)), 4),
            "val": round(len(context.val_built) / max(1, len(context.train_built) + len(context.val_built) + len(context.test_built)), 4),
            "test": round(len(context.test_built) / max(1, len(context.train_built) + len(context.val_built) + len(context.test_built)), 4),
        },
        "split_sizes": {"train": len(context.train_built), "val": len(context.val_built), "test": len(context.test_built)},
        "implicit_diagnostics": context.diagnostics,
        "benchmark_summary": benchmark["metadata"],
        "benchmark_gold_eval": benchmark["gold_metrics"],
        "benchmark_structural_audits": benchmark["structural_audits"],
        "benchmark_v2_novelty": benchmark["novelty"],
        "aspect_registry": {
            "run_registry_version": context.run_registry_version,
            "promoted_registry_version": context.promoted_registry_version,
            "run_registry_domains": len(context.run_registry.get("domains", {})),
            "promoted_registry_domains": len(context.promoted_registry.get("domains", {})),
        },
        "sentiment_quality": governance["sentiment_quality"],
        "synthetic_generation": context.synthetic_audit,
        "robust_training_eval": evaluation["robust_training_eval"],
        "promotion_guard": evaluation["promotion_guard"],
        "benchmark_artifact_counts": benchmark["metadata"].get("split_counts", {}),
        "benchmark_review_queue_rows": len(benchmark["review_queue_rows"]),
        "output_quality": context.quality_analysis_summary,
        "strict_quality": context.quality_analysis_summary.get("strict_quality", {}),
        "strict_artifacts": {
            "strict_train_rows": len(context.strict_train_export_rows),
            "strict_val_rows": len(context.strict_val_export_rows),
            "strict_test_rows": len(context.strict_test_export_rows),
            "strict_review_queue_rows": len(context.strict_review_queue_rows),
            "strict_challenge_rows": len(context.strict_challenge_rows),
        },
        "strict_floor_recovery": context.strict_floor_stats,
        "train_export_floor_rows": len(context.train_export_floor_rows),
        "grounded_prediction_rate": context.grounding["grounded_prediction_rate"],
        "ungrounded_non_general_count": context.grounding["ungrounded_non_general_count"],
        "train_general_rows_before_policy": train["general_policy_stats"]["train_general_rows_before_policy"],
        "train_general_rows_after_policy": train["general_policy_stats"]["train_general_rows_after_policy"],
        "train_general_policy_applied": train["general_policy_stats"]["train_general_policy_applied"],
        "train_review_rows_before_filter": context.train_review_filter_stats["train_review_rows_before_filter"],
        "train_review_rows_after_filter": context.train_review_filter_stats["train_review_rows_after_filter"],
        "train_review_filter_applied": context.train_review_filter_stats["train_review_filter_applied"],
        "train_review_dropped_soft_rows": len(context.train_review_dropped_soft_rows),
        "train_review_dropped_hard_rows": len(context.train_review_dropped_hard_rows),
        "train_quarantine_recoverable_rows": len(context.train_quarantine_recoverable_rows),
        "train_quarantine_recovery_stats": context.train_quarantine_stats,
        "train_viability_guard_triggered": bool(train["sentiment_constraints"].get("viability_guard_triggered", False)),
        "train_reinference_stats": train["reinference_stats"],
        "train_export_stage_counts": train["stage_counts"],
        "train_salvage_stats": context.train_salvage_stats,
        "train_leakage_filter_stats_before_salvage": context.train_leakage_filter_stats_before_salvage,
        "train_leakage_filter_stats_after_salvage": context.train_leakage_filter_stats_after_salvage,
        "train_leakage_filter_stats_after_topup": context.train_leakage_filter_stats_after_topup,
        "train_leakage_filter_stats_after_targeting": context.train_leakage_filter_stats_after_targeting,
        "train_sentiment_before_balance": context.train_sentiment_before_balance,
        "train_sentiment_after_balance": context.train_sentiment_after_balance,
        "train_sentiment_constraints": train["sentiment_constraints"],
        "train_topup_stats": train["topup_stats"],
        "train_topup_rejection_breakdown": train["topup_stats"].get("train_topup_rejection_breakdown", {}),
        "topup_effectiveness": train["topup_stats"].get("topup_effectiveness", {}),
        "size_recovery_stage": train["topup_stats"].get("size_recovery_stage", "none"),
        "size_recovery_shortfall_remaining": train["topup_stats"].get("size_recovery_shortfall_remaining", 0),
        "quality_analysis_summary": context.quality_analysis_summary,
        "train_general_dominance_rate": context.train_general_dominance_rate,
        **context.train_domain_leakage_metrics,
        **context.eval_domain_leakage_metrics,
        "train_negative_ratio": context.train_negative_ratio,
        "train_positive_ratio": context.train_positive_ratio,
        "train_target_stats": train["target_stats"],
        "gold_eval": evaluation["gold_metrics"],
        "domain_generalization": evaluation["domain_generalization"],
        "unseen_domain_metrics": evaluation["unseen_metrics"],
        "domain_prior_boost_count": context.domain_prior_boost_count,
        "domain_prior_penalty_count": context.domain_prior_penalty_count,
        "explicit_metrics": context.explicit_metrics,
        "validation": {
            "counts_match": context.counts_match,
            "has_language_distribution": bool(context.prepared_language_distribution),
            "has_candidate_aspects_by_language": bool(context.candidate_aspects_by_language),
            "no_generic_aspects": int(context.quality_analysis_summary.get("generic_implicit_aspects", 0)) == 0,
            "no_rejected_aspects": int(context.quality_analysis_summary.get("rejected_implicit_aspects", 0)) == 0,
            "has_gold_eval": bool(evaluation["gold_metrics"].get("has_gold_labels")) or bool(benchmark["gold_metrics"].get("has_gold_interpretations")),
            "has_benchmark_gold_eval": bool(benchmark["gold_metrics"].get("has_gold_interpretations")),
            "train_general_excluded": context.train_general_dominance_rate == 0.0,
            "train_domain_leakage_ok": float(context.train_domain_leakage_metrics.get("train_domain_leakage_row_rate", 1.0)) == 0.0,
            "train_target_size_within_range": bool(train["target_stats"].get("size_within_target_range")),
            "train_positive_ratio_within_max": float(context.train_positive_ratio) <= float(cfg.train_max_positive_ratio),
            "train_neutral_ratio_within_max": float(context.train_neutral_ratio) <= float(cfg.train_neutral_max_ratio),
            "train_target_blocking_failure": context.train_target_blocking_failure,
            "sampled_run_blocked_or_debug": context.sampled_run_blocked_or_debug,
            "unseen_non_general_coverage_ok": float(evaluation["unseen_metrics"].get("unseen_non_general_coverage", 0.0)) >= float(cfg.unseen_non_general_coverage_min),
            "unseen_not_ready_rate_ok": float(evaluation["unseen_metrics"].get("unseen_implicit_not_ready_rate", 1.0)) <= float(cfg.unseen_implicit_not_ready_rate_max),
            "unseen_domain_leakage_ok": float(evaluation["unseen_metrics"].get("unseen_domain_leakage_row_rate", 1.0)) <= float(cfg.unseen_domain_leakage_row_rate_max),
            "strict_explicit_contamination_ok": float(context.quality_analysis_summary.get("explicit_in_implicit_rate", 1.0)) <= float(cfg.strict_explicit_in_implicit_rate_max),
            "strict_boundary_fp_ok": int(context.quality_analysis_summary.get("boundary_false_positive_count", 10**9)) <= int(cfg.strict_boundary_fp_max),
            "strict_h2_h3_ok": float(context.quality_analysis_summary.get("h2_h3_ratio", 0.0)) >= float(cfg.strict_h2_h3_ratio_min),
            "strict_multi_aspect_ok": float(context.quality_analysis_summary.get("multi_aspect_ratio", 0.0)) >= float(cfg.strict_multi_aspect_ratio_min),
            "strict_challenge_ok": float(context.quality_analysis_summary.get("challenge_macro_f1", 0.0)) >= float(cfg.strict_challenge_macro_f1_min),
            "grouped_split_leakage_ok": int(benchmark["metadata"].get("grouped_split_leakage", {}).get("overlap_counts", {}).get("train_val", 0)) == 0
            and int(benchmark["metadata"].get("grouped_split_leakage", {}).get("overlap_counts", {}).get("train_test", 0)) == 0
            and int(benchmark["metadata"].get("grouped_split_leakage", {}).get("overlap_counts", {}).get("val_test", 0)) == 0,
            "benchmark_val_non_empty": int(len(benchmark["rows_by_split"].get("val", []))) > 0,
            "benchmark_grounded_evidence_ok": float(benchmark["metadata"].get("grounded_evidence_rate", 0.0)) >= 0.98,
            "benchmark_duplicate_rate_ok": float(benchmark["metadata"].get("duplicate_interpretation_rate", 1.0)) <= 0.01,
            "benchmark_thermal_share_ok": float(benchmark["metadata"].get("thermal_share", 1.0)) <= 0.35,
            "benchmark_domain_coverage_ok": bool(benchmark["metadata"].get("benchmark_domain_coverage_ok", True)),
            "benchmark_family_floor_ok": bool(benchmark["metadata"].get("family_floor_policy", {}).get("applied", False))
            or all(
                int(benchmark["metadata"].get("source_domain_family_counts", {}).get(domain, 0)) == 0
                or int(benchmark["metadata"].get("benchmark_domain_family_counts", {}).get(domain, 0)) > 0
                for domain in context.core_benchmark_domains
            ),
            "benchmark_implicit_purity_ok": float(benchmark["metadata"].get("implicit_purity_rate", 0.0)) >= 0.7,
            "benchmark_ontology_compatibility_ok": float(benchmark["metadata"].get("ontology_compatibility_rate", 0.0)) >= 0.9,
            "sentiment_mismatch_rate_ok": float(governance["sentiment_quality"].get("sentiment_mismatch_rate", 1.0)) <= 0.2,
            "promotion_guard_ok": not bool(evaluation["promotion_guard"].get("blocked", False)),
            "benchmark_artifact_counts_match": True,
        },
    }
    return report
